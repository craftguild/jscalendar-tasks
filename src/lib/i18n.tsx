type MessageKey = keyof typeof messages.en;

type I18nContextValue = {
  language: LanguageCode;
  setLanguage: (language: LanguageCode) => void;
  t: (key: MessageKey, values?: Record<string, string | number>) => string;
};

const I18nContext = createContext<I18nContextValue | null>(null);

/**
 * Checks whether a stored value is one of the supported language codes.
 */
function isLanguageCode(value: string): value is LanguageCode {
  return LANGUAGE_PROFILES.some((profile) => profile.code === value);
}

/**
 * Applies the active language profile to the document element.
 */
function applyLanguage(code: LanguageCode) {
  const profile = getLanguageProfile(code);
  document.documentElement.lang = profile.htmlLang;
  document.documentElement.dataset.language = profile.code;
  document.documentElement.style.setProperty(
    "--app-font-family",
    profile.fontFamily,
  );
}

/**
 * Replaces named placeholders in a localized message.
 */
function interpolate(
  message: string,
  values?: Record<string, string | number>,
) {
  if (!values) return message;
  return Object.entries(values).reduce(
    (result, [key, value]) => result.replaceAll(`{${key}}`, String(value)),
    message,
  );
}

/**
 * Provides the active language and translation helper to client components.
 */
export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<LanguageCode>(DEFAULT_LANGUAGE);

  useEffect(() => {
    const stored = window.localStorage.getItem(STORAGE_KEY);
    const next = stored && isLanguageCode(stored) ? stored : DEFAULT_LANGUAGE;
    // eslint-disable-next-line react-hooks/set-state-in-effect
    setLanguageState(next);
    applyLanguage(next);
  }, []);

  const value = useMemo<I18nContextValue>(() => {
    /**
     * Persists and applies a newly selected UI language.
     */
    function setLanguage(next: LanguageCode) {
      setLanguageState(next);
      window.localStorage.setItem(STORAGE_KEY, next);
      applyLanguage(next);
    }

    /**
     * Looks up and interpolates a localized message.
     */
    function t(key: MessageKey, values?: Record<string, string | number>) {
      const dictionary = messages[language] ?? messages[FALLBACK_LANGUAGE];
      return interpolate(
        dictionary[key] ?? messages[FALLBACK_LANGUAGE][key],
        values,
      );
    }

    return { language, setLanguage, t };
  }, [language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

/**
 * Returns the current i18n context for client components.
 */
export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used inside I18nProvider");
  }
  return context;
}

